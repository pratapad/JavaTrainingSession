package JavaBasics;

public class LoopsConcept {

	public static void main(String[] args) {
		// while loop
		//print 1 to 10 numbers
		int i=1;
		
		while(i<=10) {
			System.out.println(i);
			i=i+1;
		}
		
		//For loop 
		//Print 1 to 10 numbers
		
		for(int j=1;j<=10;j++) {
			System.out.println(j);
		}
		System.out.println("**********");

		//Print 10 to 1
		
		
		int a=10;
		while(a>=1) {
			System.out.println(a);
			a--;
		}
		System.out.println("___________");
		for(int b=10;b>=-10;b--) {
			System.out.println(b);
		}
		
	}

}
